package com.teoria;

public class Operadores {

	public static void main(String[] args) {
		
		double numero1=123.456f;
		double numero2=987.654;
		double resultados=0;
		double resultador=0;
		double resultadom=0;
		double resultadod=0;
		double resultadomod=0;
		
		//suma los numeros
		
		resultados=numero1+numero2;
		System.out.println(resultados);
		
		//resta los numeros
		
		resultador=numero1-numero2;
		System.out.println(resultador);
		
		//divide los numeros
		
		resultadod=numero1/numero2;
		System.out.println(resultadod);
		
		//multiplica los numeros
		
		resultadom=numero1*numero2;
		System.out.println(resultadom);
		
		//modulo los numeros

		resultadomod=numero1%numero2;
		System.out.println(resultadomod);
		
	}

}
